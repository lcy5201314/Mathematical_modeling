function z=MyCost3(x)

    x(2)=x(2)*60-30;

    z1=x(1);
    
    g=11+x(2)^2-10*cos(2*pi*x(2));
    
    if z1<=g
        h=1-sqrt(z1/g);
    else
        h=0;
    end

    z2=g*h;
    
    z=[z1 z2];
    
end